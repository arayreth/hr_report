            La loi n°2016-1691 du 9 décembre 2016, dite « Sapin 2 », est le cadre juridique sur lequel repose
            la plateforme Legatech. Elle oblige certaines entreprises à mettre en place des dispositifs internes
            de signalement et de conformité pour lutter contre la corruption.
        </p>
