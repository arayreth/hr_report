    <!-- 1. Origine et objectifs -->
    <div class="sapin-card">
        <div class="sapin-card-header">
            <div class="sapin-card-icon">⚖️</div>
            <h2 class="sapin-card-title"><span class="step">01</span> Origine et objectifs</h2>
        </div>
        <div class="sapin-card-body">
            <p>
                Portée par Michel Sapin, ministre de l'Économie, cette loi modernise le dispositif anticorruption
                français instauré en 1993. Elle aligne la France sur les standards internationaux (OCDE, FCPA)
                et a conduit à la création de l'<strong>Agence Française Anticorruption (AFA)</strong>, chargée de contrôler
                la bonne application de la loi.
            </p>
            <p>
                Elle vise à prévenir la corruption, le trafic d'influence, la prise illégale d'intérêt et
                tout autre manquement à la probité au sein des organisations.
            </p>
        </div>
    </div>

    <!-- 2. Qui est concerné -->
    <div class="sapin-card">
        <div class="sapin-card-header">
            <div class="sapin-card-icon">🏢</div>
            <h2 class="sapin-card-title"><span class="step">02</span> Qui est concerné ?</h2>
        </div>
        <div class="sapin-card-body">
            <p>
                La loi s'applique aux entreprises de <strong>500 salariés ou plus</strong> avec un chiffre
                d'affaires consolidé supérieur à <strong>100 millions d'euros</strong>, ainsi qu'à leurs
                filiales françaises.
            </p>
            <p>
                Pour les structures entre <strong>50 et 499 salariés</strong>, une obligation allégée s'applique :
                la mise en place d'un dispositif de recueil des signalements.
            </p>
            <div class="sapin-highlight">
                ✦ La plateforme Legatech est conçue pour répondre à cette obligation dès le seuil de 50 salariés.
            </div>
        </div>
    </div>

    <!-- 3. Dispositif de signalement -->
    <div class="sapin-card">
        <div class="sapin-card-header">
            <div class="sapin-card-icon">📋</div>
            <h2 class="sapin-card-title"><span class="step">03</span> Le dispositif de signalement</h2>
        </div>
        <div class="sapin-card-body">
            <p>
                La loi impose un canal sécurisé et confidentiel permettant à tout salarié de signaler des
                comportements contraires à la loi ou à l'éthique de l'entreprise : corruption, fraude,
                harcèlement, discrimination, etc.
            </p>
            <p>
                Legatech répond à cette exigence en permettant le dépôt de signalements à tout moment, avec
                attribution d'un <strong>code de suivi unique</strong>. Chaque dossier est traité dans un délai d'environ
                <strong>3 mois</strong>, conformément aux exigences légales.
            </p>
        </div>
    </div>

    <!-- 4. Protection du lanceur d'alerte -->
    <div class="sapin-card">
        <div class="sapin-card-header">
            <div class="sapin-card-icon">🛡️</div>
            <h2 class="sapin-card-title"><span class="step">04</span> Protection du lanceur d'alerte</h2>
        </div>
        <div class="sapin-card-body">
            <p>
                Tout salarié signalant de bonne foi est protégé contre les représailles : aucun licenciement,
                sanction ou mesure discriminatoire ne peut lui être appliqué. La confidentialité de son identité
                est garantie par la loi et ne peut être levée qu'auprès des RH et juristes habilités.
            </p>
            <p>
                La plateforme permet également un dépôt <strong>entièrement anonyme</strong>. Dans ce cas, l'identité du
                déclarant est masquée techniquement dans l'ensemble du système, y compris dans les échanges
                de messagerie.
            </p>
        </div>
    </div>

    <!-- 5. Signalements abusifs -->
    <div class="sapin-card">
        <div class="sapin-card-header">
            <div class="sapin-card-icon">⚠️</div>
            <h2 class="sapin-card-title"><span class="step">05</span> Signalements abusifs</h2>
        </div>
        <div class="sapin-card-body">
            <p>
                Tout signalement effectué de mauvaise foi ou dans le but de nuire est déclaré irrecevable.
                Son auteur s'expose à des poursuites civiles pour réparation du préjudice causé, voire à des
                poursuites pénales pour dénonciation calomnieuse.
            </p>
            <p>
                Ce dispositif est un outil de protection collective, non un moyen de règlement de comptes.
            </p>
